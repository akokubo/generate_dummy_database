# ランダムに名前を生成する関数
def random_name():
    # ランダムに姓と名を選択
    first = random.randrange(0, 50)  # 名のインデックス
    last = random.randrange(0, 50)   # 姓のインデックス

    # 姓と名、カタカナ名を取り出す
    last_name = last_names[last]
    first_name = first_names[first]
    last_name_katakana = last_names_katakana[last]
    first_name_katakana = first_names_katakana[first]

    # フルネームを作成（漢字とカタカナ両方）
    name = f'{last_name} {first_name}'
    name_katakana = f'{last_name_katakana} {first_name_katakana}'

    return name, name_katakana

# ランダムに日付を生成する関数
def random_date():
    # start_dateからend_dateの範囲内でランダムな日付を生成
    date = start_date + timedelta(days=random.randint(0, (end_date - start_date).days))
    return date

# ランダムに学科を選択する関数
def random_department():
    # 学科のリストからランダムに1つ選択
    r = random.choice(departments)
    return r

# ランダムに生成した名前、カナ、誕生日、学科をリストに格納
data = []
for i in range(300):  # 300件のデータを生成
    名前, カナ = random_name()  # ランダムに名前を生成
    誕生日 = random_date()     # ランダムに誕生日を生成
    学科 = random_department()  # ランダムに学科を選択
    data.append((名前, カナ, 誕生日, 学科))  # 生成したデータをリストに追加

# 生年月日でソート（昇順）
data_sorted = sorted(data, key=lambda x: x[2])

# ソートした結果を表示
